public class Subject {
    private String subName;
    private int marks;

    public void setSubjectName(String subName) {
        this.subName = subName;
    }

    public void setMarks(int marks) {
        this.marks = marks;
    }

    public String getSubjectName() {
        return subName;
    }

    public int getMarks() {
        return marks;
    }
}
